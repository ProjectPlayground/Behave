public class Person 
{
	/* Variables */
	protected String name;
	
	/* Constructors */
	
	public Person(String name)
	{
		this.name = name;
	}
	
	/* Getters */
	public String getName()
	{
		return name;
	}
	
	/* Setters */
	public void setName(String name)
	{
		this.name = name;
	}
}
